class Product {
  final String name;
  final String category;
  final String brand;
  final String unit;
  final String price;
  final String location;
  final String addedDate;
  final List<String> images;

  Product({
    required this.name,
    required this.category,
    required this.brand,
    required this.unit,
    required this.price,
    required this.location,
    required this.addedDate,
    required this.images,
  });
}